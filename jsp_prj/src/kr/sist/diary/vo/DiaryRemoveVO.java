package kr.sist.diary.vo;

public class DiaryRemoveVO {

	private int num;
	private String pass;
	public DiaryRemoveVO() {
		super();
	}
	public DiaryRemoveVO(int num, String pass) {
		super();
		this.num = num;
		this.pass = pass;
	}
	public int getNum() {
		return num;
	}
	public String getPass() {
		return pass;
	}
	
	
}
